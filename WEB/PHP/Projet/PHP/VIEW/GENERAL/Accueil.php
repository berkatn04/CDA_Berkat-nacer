        <h1 class="center">HISTOIRE DU ZOO</h1>
        <div class="colonne">
            <p>
            Fondé en 1946, le Zoo de La Flèche fut le premier zoo privé à voir le jour en France. Près d’un siècle plus tard,
             il se distingue toujours et fait partie des 5 premiers parcs zoologiques français en nombre de visiteurs.
            </p>
            <p>
            Au travers de la qualité de ses installations et le travail de ses équipes,
             le parc porte toute son attention et ses efforts au développement du bien-être des animaux et à l’accueil des visiteurs.
            </p>
            <p>
            Chaque jour, ses équipes mettent tout en œuvre pour faire du Zoo un espace de convivialité en perpétuelle évolution, dans le respect de ses pensionnaires.
             Cet engagement se caractérise notamment au travers de quatre missions clés : l’éducation du public, sa distraction grâce à des animations novatrices et porteuses de sens pour les animaux,
             mais également la conservation des espèces, et la recherche in situ et ex situ.
            </p>
