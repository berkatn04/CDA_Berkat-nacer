        </div>
        <h4>LA PRÉSERVATION DE LA FAUNE, MAIS AUSSI DE LA FLORE</h4>
        <div class="colonne">
            <p>Particularité du Zoo de La Flèche : la diversité des espèces végétales qu’il abrite.
                Le décor végétal contribue naturellement au bien-être des animaux et indirectement à celui des visiteurs.
            </p>
            <p>Conscient des enjeux que représente la flore pour ses pensionnaires, le Zoo met l’accent sur sa préservation et compte parmi ses décors,
                plus de 800 espèces végétales, toutes soigneusement positionnées en fonction des ambiances recréées pour les animaux.
            </p>
        </div>
        <h4>LE ZOO DE LA FLÈCHE C’EST :</h4>
        <div>
            <ul>
                <li>Plus de 400 000 visiteurs par an</li>
                <li>18 hectares dédiés à la visite</li>
                <li>90 tonnes de fruits et légumes par an</li>
            </ul>
        </div>
        <p>Mais aussi :</p>
        <div>
            <ul>
                <li>1500 animaux de 160 espèces différentes qui évoluent sur le parc</li>
                <li>100 personnes qui œuvrent chaque jour pour le bien-être des animaux et l’entretien du parc</li>
                <li>Des spectacles et animations assurés tout au long de la saison par les équipes du Zoo</li>
                <li>Environ 100 naissances par an</li>
            </ul>
        </div>
        <p>Et bien plus encore …</p>
        <div>
            <ul>
                <li>Un rapport exceptionnel à l’animal : mise en place de techniques de rares pour soigner les animaux (medical training)</li>
                <li>Pour la 1ère fois, la possibilité de dormir avec des tigres blancs, des loups arctiques, des lémuriens, des ours polaires et des grizzlys</li>
                <li>Des installations remarquables au service du bien-être des animaux</li>
            </ul>
        </div>
        <h4>Des installations remarquables au service du bien-être des animaux</h4>
        <div class="colonne">
            <p>« Le Zoo de La Flèche écrit actuellement l’une des pages les plus passionnantes et les plus stimulantes de son histoire. 
                Cette aventure est avant tout une rencontre de femmes et d’hommes animés d’une passion commune et désireux d’atteindre
                l’excellence dans l’accomplissement des missions du parc : émerveillement des visiteurs, éducation, recherche et conservation.
            </p>
            <p>« Une saison au zoo » témoigne aujourd’hui de notre souci permanent du bien-être de nos animaux et de notre implication sans faille dans la préservation de la biodiversité, menacée de toutes parts.
                 A travers les projets qu’il porte, notre zoo fait maintenant un pari pour l’avenir : celui que notre créativité, nos valeurs de générosité, d’altruisme, d’optimisme et d’engagement total aux côtés des acteurs de la conservation,
                  touchent le jeune public, cette génération future qui demain aura la lourde responsabilité de soigner, préserver, notre si belle planète. »</p>
